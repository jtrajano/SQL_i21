CREATE VIEW [dbo].[vyuDBPanel]
	AS 
SELECT A.[intPanelId]
      ,A.[intRowsReturned]
      ,A.[intRowsVisible]
      ,A.[intChartZoom]
      ,A.[intChartHeight]
      ,A.[intUserId]
      ,A.[intDefaultColumn]
      ,A.[intDefaultRow]
      ,A.[intDefaultWidth]
      ,A.[intSourcePanelId]
      ,A.[intConnectionId]
	  ,B.strName as strConnectionName
      ,A.[intDrillDownPanel]
      ,A.[strClass]
      ,A.[strPanelName]
      ,A.[strStyle]
      ,A.[strAccessType]
      ,A.[strCaption]
      ,A.[strChart]
      ,A.[strChartPosition]
      ,A.[strChartColor]
      ,A.[strDateCondition]
      ,A.[strDateCondition2]
      ,A.[strDateFieldName]
      ,A.[strDateFieldName2]
      ,A.[strDataSource]
      ,A.[strDataSource2]
      ,A.[strDateVariable]
      ,A.[strDateVariable2]
      ,A.[strDefaultTab]
      ,A.[strDescription]
      ,A.[strPanelNameDuplicate]
      ,A.[strPanelType]
      ,A.[strQBCriteriaOptions]
	  ,A.[strFilterCondition]
      ,C.[strFilter] as strOtherFilter
      ,A.[strFilterVariable]
      ,A.[strFilterFieldName]
      ,A.[strFilterVariable2]
      ,A.[strFilterFieldName2]
      ,A.[strGroupFields]
      ,A.[strFilters]
      ,A.[strConfigurator]
      ,A.[ysnChartLegend]
      ,A.[ysnShowInGroups]
      ,A.[imgLayoutGrid]
      ,A.[imgLayoutPivotGrid]
      ,A.[strPanelVersion]
      ,A.[intFilterId]
      ,A.[intConcurrencyId ]
      ,A.[intCannedPanelId]
      ,A.[strSortValue]
      ,A.[ysnAutoRefresh]
      ,A.[intAutoRefreshInterval]
      ,A.[strOrderByVariable]
      ,A.[strOrderByFieldName]
      ,A.[ysnOrderByFieldDescending]
	  ,D.[strPanelName] as strDrillDownName
	  ,A.[strTableName]
	  ,A.[strColumnName]
	  ,A.[ysnSystemPanel]
	  ,A.[ysnSystemPanelChild]
  FROM [dbo].[tblDBPanel] A
  LEFT JOIN [dbo].[tblRMConnection] B ON B.intConnectionId = A.intConnectionId
  LEFT JOIN [dbo].[tblDBPanelFilter] C ON C.intFilterId = A.intFilterId
  LEFT JOIN tblDBPanel D ON D.intPanelId = A.intDrillDownPanel
