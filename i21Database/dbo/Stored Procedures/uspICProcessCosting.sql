/*
	This is the main stored procedure used for handling the inventory costing. 
*/

CREATE PROCEDURE [dbo].[uspICProcessCosting]
	@ItemsToProcess AS ItemCostingTableType READONLY
	,@strBatchId AS NVARCHAR(20)
	,@ysnPost AS BIT
	,@intTransactionId INT
	,@intTransactionTypeId INT
AS

SET QUOTED_IDENTIFIER OFF
SET ANSI_NULLS ON
SET NOCOUNT ON
SET XACT_ABORT ON
SET ANSI_WARNINGS OFF

/* Check if we are doing a post */
IF @ysnPost = 1 
BEGIN 
	BEGIN TRY
		-- Do the Validation
		EXEC [dbo].[uspICValidateCostingOnPost] @ItemsToProcess

		-- Post the items to the costing tables and generate financials (g/l entries)
		EXEC [dbo].[uspICPostCosting] @ItemsToProcess, @strBatchId

	END TRY
	BEGIN CATCH
		-- Call the item costing error handler
		EXEC [dbo].[uspICFoundCostingErrors]
		RETURN;
	END CATCH 

END

/* Check if we are doing an unpost*/
ELSE IF @ysnPost = 0
BEGIN 
	BEGIN TRY
		-- Validate if the transaction can be reversed. 
		EXEC [dbo].[uspICValidateCostingOnUnpost] @intTransactionId, @intTransactionTypeId

		-- Reverse the stocks and generate the financials (g/l entries)
		EXEC [dbo].[uspICUnpostCosting] @strBatchId, @intTransactionId, @intTransactionTypeId

		-- The G/L Reversals are generated by the calling stored procedure. 
	END TRY
	BEGIN CATCH
		-- Call the item costing error handler
		EXEC [dbo].[uspICFoundCostingErrors]
		RETURN;
	END CATCH 

END 

