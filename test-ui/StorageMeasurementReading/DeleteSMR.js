StartTest (function (t) {
    new iRely.FunctionalTest().start(t)

        //region Scenario 1. Delete Storage Measurement Reading
        .displayText('===== Scenario 1. Delete Storage Measurement Reading ====')
        .clickMenuFolder('Inventory','Folder')
        .clickMenuScreen('Storage Measurement Reading','Screen')
        .waitUntilLoaded()
        .doubleClickSearchRowValue('0001 - Fort Wayne', 'strLocation', 3)
        .waitUntilLoaded()
        .verifyGridData('StorageMeasurementReading', 1, 'colItem', '001 - CLTI')
        .verifyGridData('StorageMeasurementReading', 1, 'colSubLocation', 'Raw Station')
        .clickButton('Delete')
        .waitUntilLoaded()
        .verifyMessageBox('iRely i21','Are you sure you want to delete this record?','yesno', 'question')
        .clickMessageBoxButton('yes')
        .waitUntilLoaded()
        .displayText('===== Scenario 1. Delete Storage Measurement Reading Done ====')


        .displayText('=== Scenario 2: Delete a single item in a Storage Measurement Reading ====')
        .waitUntilLoaded()
        .doubleClickSearchRowValue('0001 - Fort Wayne', 'strLocation', 1)
        .waitUntilLoaded()
        .selectGridRowNumber('StorageMeasurementReading',[1])
        .clickButton('Remove')
        .waitUntilLoaded()
        //.verifyMessageBox('iRely i21','You are about to delete 1 row.<br/>Are you sure you want to continue?','yesno', 'question')
        .clickMessageBoxButton('yes')
        .waitUntilLoaded('')
        .clickButton('Save')
        .verifyStatusMessage('Saved')
        .waitUntilLoaded()
        .clickButton('Close')
        .waitUntilLoaded()
        .clearTextFilter('FilterGrid')
        .displayText('=== Scenario 2: Delete a single item in a Storage Measurement Reading Done ====')

        .displayText('=== Scenario 3: Delete multiple items in a Storage Measurement Reading====')
        .waitUntilLoaded()
        .doubleClickSearchRowValue('0001 - Fort Wayne', 'strLocation', 1)
        .waitUntilLoaded()
        .selectGridRowNumber('StorageMeasurementReading',[1,2])
        .clickButton('Remove')
        .waitUntilLoaded()
        //.verifyMessageBox('iRely i21','You are about to delete 2 rows.<br/>Are you sure you want to continue?','yesno', 'question')
        .clickMessageBoxButton('yes')
        .waitUntilLoaded('')
        .clickButton('Save')
        .verifyStatusMessage('Saved')
        .waitUntilLoaded()
        .clickButton('Close')
        .waitUntilLoaded()
        .clearTextFilter('FilterGrid')
        .displayText('=== Scenario 3: Delete a single item in a Storage Measurement Reading Done ====')

        .displayText('=== Scenario 4: Delete multiple Storage Measurement Reading ====')
        .waitUntilLoaded()
        .selectSearchRowNumber([1,2])
        .clickButton('OpenSelected')
        .waitUntilLoaded()
        .clickButton('Delete')
        .waitUntilLoaded()
        .verifyMessageBox('iRely i21','Are you sure you want to delete this record?','yesno', 'question')
        .clickMessageBoxButton('yes')
        .waitUntilLoaded()
        .clickButton('Delete')
        .waitUntilLoaded()
        .verifyMessageBox('iRely i21','Are you sure you want to delete this record?','yesno', 'question')
        .clickMessageBoxButton('yes')
        .waitUntilLoaded()
        .displayText('=== Scenario 4: Delete multiple Storage Measurement Reading ====')


        .done();

})