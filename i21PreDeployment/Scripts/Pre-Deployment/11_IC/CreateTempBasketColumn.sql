PRINT N'BEGIN - Add TEMP ysnIsBasket to tblICItem'
GO
IF EXISTS(SELECT TOP 1 1 FROM INFORMATION_SCHEMA.COLUMNS WHERE [TABLE_NAME] = N'tblICItem' AND [COLUMN_NAME] = N'ysnIsBasket')
BEGIN
	EXEC('
		IF EXISTS(SELECT TOP 1 1 FROM tblICItem WHERE ysnIsBasket = 1)
		BEGIN
			ALTER TABLE [dbo].[tblICItem]
				ADD [ysnIsBasket_TEMP] BIT NULL;
		END
		')
	EXEC('
		IF EXISTS(SELECT TOP 1 1 FROM INFORMATION_SCHEMA.COLUMNS WHERE [TABLE_NAME] = N''tblICItem'' AND [COLUMN_NAME] = N''ysnIsBasket_TEMP'')
		BEGIN
			UPDATE [dbo].[tblICItem] SET [ysnIsBasket_TEMP] = [ysnIsBasket];
		END
	')
END
GO
PRINT N'END - Add TEMP ysnIsBasket to tblICItem'